# SECTION 6: IN AND NOT IN OPERATORS - Practice

# Q1: Create the text variable
# Example: text = 'Hello World'
text = 'Hello World'


# Q2: Check if substring exists
# Example: print('Hello' in text)      # True
# Example: print('Goodbye' in text)    # False
print('Hello' in text)



# Q3: Check if substring does NOT exist
# Example: print('Goodbye' not in text)  # True
# Example: print('Hello' not in text)    # False
print('Goodbye' not in text)



# Q4: Case matters!
# Example: print('hello' in text)      # False (lowercase 'h')
print('hello world' not in text) 


