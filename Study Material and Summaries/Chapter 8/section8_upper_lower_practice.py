# Q2: Create variable 'title' with value 'LEARNING CODE', print it in all lowercase
# Example: name = 'ALICE'
# Example: print(name.lower())
title = 'LEARNING CODE'
print(title.lower())


# Q3: Create variable 'user_answer' with value 'Yes', check if it equals 'yes' (case-insensitive) and print True or False
# Example: response = 'NO'
# Example: print(response.lower() == 'no')
