# SECTION 1: STRING LITERALS - Practice

# Q1: Single quotes
# Example: spam = 'Hello'
# Example: print(spam)
greeting = 'Good morning'
print(greeting)


# Q2: Double quotes
# Example: spam = "Hello"
# Example: print(spam)
greeting = "Good morning"
print(greeting)


# Q3: Apostrophe in string (using double quotes)
# Example: message = "Alice's cat"
# Example: print(message)
message = "Bob's dog"
print(message)


# Q4: Using single quotes with escaped apostrophe
# Example: message = 'Alice\'s cat'
# Example: print(message)
message = 'Bob\'s dog'
print(message)



