# Chapter 8 - Section 4: Multiline Strings Reference

# Example 1: Basic multiline string with triple single quotes
text = '''This is a
multiline
string.'''
print(text)

print()  # Blank line for separation

# Example 2: Triple double quotes work too
message = """Dear Alice,

How are you?

Sincerely,
Bob"""
print(message)

print()  # Blank line for separation

# Example 3: Useful for long paragraphs
paragraph = '''Python is a great language.
It has many features.
This string preserves line breaks.'''
print(paragraph)
