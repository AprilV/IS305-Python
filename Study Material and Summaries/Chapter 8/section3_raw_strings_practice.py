# SECTION 3: RAW STRINGS - Practice

# Q1: Normal string - needs double backslashes
# Example: print('C:\\Users\\Alice\\Documents')
print('C:\\Projects\\Python')


# Q2: Raw string - backslashes work as-is
# Example: print(r'C:\Users\Alice\Documents')
print(r'C:\Projects\Python')


# Q3: Raw strings useful for file paths
# Example: path = r'C:\Users\Alice\Desktop\file.txt'
# Example: print(path)
path = r'C:\Users\Bob\Desktop\report.txt'
print(path)



